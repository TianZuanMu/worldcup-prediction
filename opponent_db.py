def check_three_conditions(team_name: str, gap_level: str = "moderate") -> dict:
    data = opponent_quality(team_name)
    a_pass = data['rank'] >= 50
    fw_count, mf_count, threat_level, scorers, pre_goals = _count_attacking_threat(team_name, gap_level)
    if len(scorers) == 0: b_pass = True
    elif threat_level < 1.0: b_pass = True
    elif threat_level < 1.5 and pre_goals < 1.2: b_pass = True
    else: b_pass = False
    current_year = 2026; recent_killings = []; old_killings = []
    for gk in data.get('giant_killings', []):
        # Extract all 4-digit years from the string
        digits = ''.join(c for c in str(gk) if c.isdigit())
        years = [int(digits[i:i+4]) for i in range(0, len(digits), 4)] if len(digits) >= 4 else []
        # If any year is within 3 years, it's recent
        has_recent = any(y > current_year - 5 for y in years) if years else False  # 🆕 V3.33: 3→5年
        if has_recent: recent_killings.append(gk)
        elif years: old_killings.append(gk)
        else: old_killings.append(gk)  # no year = assume old
    # 🆕 V3.33: 5年窗口 (原V3.5用3年, 但2022世界杯距今4年仍具参考价值)
    c_pass = len(recent_killings) == 0
    passed = sum([a_pass, b_pass, c_pass])
    fail_reasons = []
    if not a_pass: fail_reasons.append(f"rank{data['rank']}<50")
    if not b_pass: fail_reasons.append(f"scorers: {', '.join(scorers)}")
    if not c_pass:
        parts = []
        if recent_killings: parts.append(f'2023+: {", ".join(recent_killings)}')
        if old_killings: parts.append(f'history: {", ".join(old_killings)}')
        fail_reasons.append(f"giant: {' | '.join(parts)}")
    rule = 'hot_win_exception' if passed == 3 else 'default_hot_lose'
    return {'all_pass': passed == 3, 'passed': passed, 'total': 3,
        'conditions': {'a_rank60plus': a_pass, 'b_no_top5_scorer': b_pass, 'c_no_giant_killing': c_pass},
        'fail_reason': ' | '.join(fail_reasons) if fail_reasons else 'all_pass',
        'rule': rule, 'quality_score': 3 - passed, 'scorers': scorers,
        'threat_level': threat_level, 'data': data,
        'recent_killings': recent_killings, 'old_killings': old_killings}  # 🆕 V3.32


def check_moderate_opponent(team_name: str, gap_level: str = "moderate", fav_rank: int = 99) -> dict:
    """V3.5: 精英强队(rank≤5)对手威胁判定门槛提高"""
    fw, mf, threat, scorers, pre_goals = _count_attacking_threat(team_name, gap_level)
    # V3.5: Elite teams face a higher bar for opponent threat
    if fav_rank <= 5:
        # Elite: need REAL attacking quality — at least 1 top5 FW with value >= 20M
        # or 1 elite attacking MF (value >= 50M)
        has_elite = False
        data = opponent_quality(team_name)
        for p in data.get('players', []):
            if isinstance(p, dict) and p.get('top5') and p.get('pos') in ('FW',):
                if p.get('value_m', 0) >= 20:
                    has_elite = True
                    break
            if isinstance(p, dict) and p.get('top5') and p.get('pos') == 'MF':
                if p.get('value_m', 0) >= 50:
                    has_elite = True
                    break
        has_threat = threat >= 3.0 and pre_goals >= 1.8 and has_elite
    else:
        has_threat = threat >= 1.5 and pre_goals >= 1.5
    return {'has_goal_threat': has_threat, 'top_players': scorers,
        'goals_per_game': pre_goals, 'threat_level': threat,
        'rule': 'hot_lose' if has_threat else 'hot_win'}
